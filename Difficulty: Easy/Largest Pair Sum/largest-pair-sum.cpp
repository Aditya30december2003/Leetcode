class Solution {
  public:
    int pairsum(vector<int> &arr) {
        // code here
        priority_queue<int>q;
        for(int&num:arr)
        q.push(num);
        
        int sum=0;
        int k=2;
        while(k--){
            sum+=q.top();
            q.pop();
        }
        return sum;
    }
};


